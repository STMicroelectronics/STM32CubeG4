/**
  * @brief  The application entry point.
  * @retval int
  */
int main(void)
{
  /* USER CODE BEGIN 1 */

  /* USER CODE END 1 */

  /* MCU Configuration--------------------------------------------------------*/

  /* Reset of all peripherals, Initializes the Flash interface and the Systick. */
  HAL_Init();

  /* USER CODE BEGIN Init */

  /* USER CODE END Init */

  /* Configure the system clock */
  SystemClock_Config();

  /* USER CODE BEGIN SysInit */

  /* USER CODE END SysInit */

  /* Initialize all configured peripherals */
  MX_GPIO_Init();
  MX_HRTIM1_Init();
  MX_ADC1_Init();
  /* USER CODE BEGIN 2 */
  BSP_LED_Init(LED4);
  BSP_LED_Init(LED3);
  BSP_LED_Init(LED5);

  BSP_JOY_Init(JOY1, JOY_MODE_GPIO, JOY_SEL);

  /* Run the ADC1 calibration in single-ended mode */
  HAL_ADCEx_Calibration_Start(&hadc1, ADC_SINGLE_ENDED);

  /* Start ADC1 Injected Conversions */
  HAL_ADCEx_InjectedStart(&hadc1);

  /* Demo starts by de-energizing output capacitor, should the demo be in boost
  mode when the MCU is reset */
  DemoState = DE_ENERGIZE;

  /* HRTIM start-up */
  /* Enable HRTIM's outputs TC1, TC2, TD1 and TD2 */
  LL_HRTIM_EnableOutput(HRTIM1,
                          LL_HRTIM_OUTPUT_TC1
                        | LL_HRTIM_OUTPUT_TC2
                        | LL_HRTIM_OUTPUT_TD1
                        | LL_HRTIM_OUTPUT_TD2);

  LL_HRTIM_EnableIT_REP(HRTIM1, LL_HRTIM_TIMER_C);
  LL_HRTIM_EnableIT_REP(HRTIM1, LL_HRTIM_TIMER_D);
  LL_HRTIM_TIM_CounterEnable(HRTIM1, LL_HRTIM_TIMER_C);
  LL_HRTIM_TIM_CounterEnable(HRTIM1, LL_HRTIM_TIMER_D);

  /* USER CODE END 2 */

  /* Infinite loop */
  /* USER CODE BEGIN WHILE */
  while (1)
  {
    /* USER CODE END WHILE */

    /* USER CODE BEGIN 3 */

    /* ---------------- */
    /* Fault management */
    /* ---------------- */
    if (LL_HRTIM_IsActiveFlag_FLT2(HRTIM1))
      DemoState = FAULT;

    /* -------------------------------------------------------------------------*/
    /* Input and output voltages measures (can be displayed in a watch window)  */
    /* -------------------------------------------------------------------------*/
    Vin = (HAL_ADCEx_InjectedGetValue(&hadc1, ADC_INJECTED_RANK_1) * VDDA)/0xFFF;
    /* VIN bridge conversion is 4.97 (6.8K / 6.8K + 27K) */
    Vin = (497 * Vin )/100; /* Values in mV (for debug convenience) */

    Vout = (HAL_ADCEx_InjectedGetValue(&hadc1, ADC_INJECTED_RANK_2) * VDDA)/0xFFF;
    /* VOUT bridge conversion is 5.03 (3.3K / 3.3K + 13.3K) */
    Vout = (503 * Vout)/100; /* Values in mV (for debug convenience) */

    /* ---------------- */
    /* Normal operation */
    /* ---------------- */
    switch(DemoState)
    {
    case BUCK:
      /* Duty cycle is modified in the TIMC IRQ */
      /* BOOST cannot be started if VIN value is too high (Vout must not exceed 15V)*/
      if ((BSP_JOY_GetState(JOY1) == JOY_SEL) && (Vin < OVER_VOLTAGE_PROTECTION))
      {
        DemoState = BOOST;
        while(BSP_JOY_GetState(JOY1) == JOY_SEL);
      }
      BSP_LED_Toggle(LED4);
      BSP_LED_Off(LED3);
      break;

    case BOOST:
      /* Vout capacitor must be de-energized before switching to BUCK mode
      otherwise causing current to flow back into voltage source */
      if (BSP_JOY_GetState(JOY1) == JOY_SEL)
      {
        DemoState = DE_ENERGIZE;
        while(BSP_JOY_GetState(JOY1) == JOY_SEL);
      }
      BSP_LED_Toggle(LED3);
      BSP_LED_Off(LED4);
      break;

    case DE_ENERGIZE:
      /* Once Vout is below a safe level (below Vin value), BUCK operation can be started */
      if (Vout < DE_ENERGIZING_THRESHOLD)
      {
        DemoState = BUCK;
      }
      BSP_LED_Off(LED3);
      BSP_LED_Off(LED5);
      break;

    case FAULT:
      /* If fault is triggered, operation can be re-started with the push-button */
      if (BSP_JOY_GetState(JOY1) == JOY_SEL)
      {
        LL_HRTIM_ClearFlag_FLT2(HRTIM1);
        LL_HRTIM_EnableOutput(HRTIM1,
                               LL_HRTIM_OUTPUT_TC1
                             | LL_HRTIM_OUTPUT_TC2
                             | LL_HRTIM_OUTPUT_TD1
                             | LL_HRTIM_OUTPUT_TD2);

        DemoState = DE_ENERGIZE;
        while(BSP_JOY_GetState(JOY1) == JOY_SEL);
      }
      BSP_LED_Toggle(LED5);
      BSP_LED_Off(LED4);
      BSP_LED_Off(LED3);
      break;

    default:
      LL_HRTIM_DisableOutput(HRTIM1,
                            LL_HRTIM_OUTPUT_TC1
                          | LL_HRTIM_OUTPUT_TC2
                          | LL_HRTIM_OUTPUT_TD1
                          | LL_HRTIM_OUTPUT_TD2);
      break;
    }

    /* This delay defines the main loop time and LED blinking rate*/
    HAL_Delay(50);

  }
  /* USER CODE END 3 */
}

