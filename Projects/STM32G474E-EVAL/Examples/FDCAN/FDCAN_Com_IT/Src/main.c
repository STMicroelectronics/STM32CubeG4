/**
  * @brief  Rx FIFO 0 callback.
  * @param  hfdcan pointer to an FDCAN_HandleTypeDef structure that contains
  *         the configuration information for the specified FDCAN.
  * @param  RxFifo0ITs indicates which Rx FIFO 0 interrupts are signaled.
  *         This parameter can be any combination of @arg FDCAN_Rx_Fifo0_Interrupts.
  * @retval None
  */
void HAL_FDCAN_RxFifo0Callback(FDCAN_HandleTypeDef *hfdcan, uint32_t RxFifo0ITs)
{
  if((RxFifo0ITs & FDCAN_IT_RX_FIFO0_NEW_MESSAGE) != 0)
  {
    /* Retrieve Rx messages from RX FIFO0 */
    if (HAL_FDCAN_GetRxMessage(hfdcan, FDCAN_RX_FIFO0, &RxHeader, RxData) != HAL_OK)
    {
      Error_Handler();
    }

    /* Compare payload to expected data */
    if (BufferCmp8b(TxData, RxData, 16) != 0)
    {
      Error_Handler();
    }
    else
    {
      if (hfdcan->Instance == FDCAN1)
      {
        /* Turn LED1 on */
        BSP_LED_On(LED1);
      }
      else /* FDCAN2 */
      {
        /* Turn LED4 on */
        BSP_LED_On(LED4);
      }
    }
  }
}

/**
  * @brief EXTI line detection callback
  * @param GPIO_Pin: Specifies the pins connected EXTI line
  * @retval None
  */
void HAL_GPIO_EXTI_Callback(uint16_t GPIO_Pin)
{
  if (GPIO_Pin == USER_BUTTON_PIN)
  {
    /* Turn LED1 and LED4 off */
    BSP_LED_Off(LED1);
    BSP_LED_Off(LED4);

    /* Check which FDCAN instance is selected to send message */
    if (Instance == 1)
	{
      /* Add message to TX FIFO of FDCAN instance 1 */
      if (HAL_FDCAN_AddMessageToTxFifoQ(&hfdcan1, &TxHeader, TxData) != HAL_OK)
      {
        Error_Handler();
      }

      /* Toggle FDCAN instance */
      Instance = 2;
    }
    else /* Instance = 2 */
    {
      /* Add message to TX FIFO of FDCAN instance 2 */
      if (HAL_FDCAN_AddMessageToTxFifoQ(&hfdcan2, &TxHeader, TxData) != HAL_OK)
      {
        Error_Handler();
      }

      /* Toggle FDCAN instance */
      Instance = 1;
    }

    /* Delay to avoid rebound */
    HAL_Delay(100);
  }
}

/**
  * @brief Compares two buffers.
  * @par Input
  *  - pBuffer1, pBuffer2: buffers to be compared.
  *  - BufferLength: buffer's length
  * @par Output
  * None.
  * @retval
  *   0: pBuffer1 identical to pBuffer2
  *   1: pBuffer1 differs from pBuffer2
  */
static uint32_t BufferCmp8b(uint8_t* pBuffer1, uint8_t* pBuffer2, uint16_t BufferLength)
{
  while(BufferLength--)
  {
    if(*pBuffer1 != *pBuffer2)
    {
      return 1;
    }

    pBuffer1++;
    pBuffer2++;
  }
  return 0;
}
