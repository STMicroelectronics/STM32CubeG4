/**
  * @brief  The application entry point.
  * @retval int
  */
int main(void)
{
  /* USER CODE BEGIN 1 */
  /* USER CODE END 1 */

  /* MCU Configuration--------------------------------------------------------*/

  /* Reset of all peripherals, Initializes the Flash interface and the Systick. */
  HAL_Init();

  /* USER CODE BEGIN Init */

  /* USER CODE END Init */

  /* Configure the system clock */
  SystemClock_Config();

  /* USER CODE BEGIN SysInit */

  /* USER CODE END SysInit */

  /* Initialize all configured peripherals */
  MX_GPIO_Init();
  MX_DAC1_Init();
  /* USER CODE BEGIN 2 */
  
  /* Initialize LED on board */
  BSP_LED_Init(LED1);
  
  /* Configure User push-button in Interrupt mode */
  BSP_PB_Init(BUTTON_USER, BUTTON_MODE_EXTI);
  /* USER CODE END 2 */

  /* Infinite loop */
  /* USER CODE BEGIN WHILE */
  
  /* Set initial DAC values to convert */
  if (HAL_DACEx_DualSetValue(&hdac1, DAC_ALIGN_12B_R,wave1[waveIndex], wave2[waveIndex]) != HAL_OK)
  {
    /* DAC value set error */
    Error_Handler();
  }
  
  /* Start DAC converters */
  if (HAL_DACEx_DualStart(&hdac1) != HAL_OK)
  {
    /* DAC conversion start error */
    Error_Handler();
  }
  
  BSP_LED_On(LED1);
  while (1)
  {
    /* Wait for event on push button to perform following actions */
    while ((ubUserButtonClickEvent) == RESET)
    {
    }
    /* Reset variable for next loop iteration (with debounce) */
    HAL_Delay(200);
    ubUserButtonClickEvent = RESET;
    
    waveIndex++;
    if (waveIndex >= sizeof(wave1)/sizeof(wave1[0])) waveIndex = 0;
    
    /* Set new DAC values to convert */
    if (HAL_DACEx_DualSetValue(&hdac1, DAC_ALIGN_12B_R,wave1[waveIndex], wave2[waveIndex]) != HAL_OK)
    {
      /* DAC value set error */
      Error_Handler();
    }
    /* USER CODE END WHILE */

    /* USER CODE BEGIN 3 */
  }
  /* USER CODE END 3 */
}

